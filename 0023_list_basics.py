# 0023_list_basics.py (리스트 기초)
# 목적: 리스트(List)를 생성하고, 인덱스(Index)를 통해 특정 위치의 데이터에 접근하기
# -------------------------------------------------------
# Scenario:
# - 이번 전시회에 사용할 배경음악(BGM) 리스트를 관리하는 시스템을 구축함.
# - 여러 개의 곡 제목을 하나의 변수(리스트)에 순서대로 담아둠.
# - 컴퓨터는 숫자를 0부터 센다는 점을 활용해, 원하는 위치의 곡을 콕 집어 출력함.
# - 리스트의 전체 개수를 파악하여 전시 분량을 확인함.
# -------------------------------------------------------

# 1. 플레이리스트 리스트 생성 (List Creation)
# 대괄호([])를 사용하여 여러 데이터를 순서대로 묶어줍니다.
playlist = ["Opening Theme", "Midpoint Highlight", "Closing Melody"]

# 2. 인덱스를 이용한 데이터 접근 (Indexing)
# 💡 파이썬의 인덱스는 0부터 시작합니다. (0: 첫 번째, 1: 두 번째, 2: 세 번째...)
first_track = playlist[0]  # 첫 번째 곡
third_track = playlist[2]  # 세 번째 곡

print(f"오프닝 곡: {first_track}")
print(f"중반 하이라이트 곡: {third_track}")

# 3. 전체 데이터 개수 확인 (Length)
# len() 함수는 리스트 안에 담긴 항목의 총 개수를 반환합니다.
total_tracks = len(playlist)

print(f"현재 플레이리스트에는 총 {total_tracks}곡이 등록되어 있습니다.")