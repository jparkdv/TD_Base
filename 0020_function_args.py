# 0020_function_args.py
# 목적: 별표(*)를 활용하여 개수가 정해지지 않은 여러 개의 데이터를 한 번에 입력받기
# -------------------------------------------------------
# Scenario:
# - 거대한 스케일의 사운드트랙을 믹싱하는 마스터 콘솔 시스템을 구축함.
# - 입력되는 오디오 트랙의 수(바이올린, 신스, 베이스, FX 등)는 매번 달라짐.
# - 함수는 트랙 개수에 상관없이 모든 볼륨 데이터를 받아 '전체 볼륨의 합'을 계산함.
# - 계산된 총합을 통해 오디오가 찢어지는(Clipping) 현상을 사전에 방지함.
# -------------------------------------------------------

# 1. 마스터 오디오 믹서 엔진 정의 (Master Audio Mixer)
# 💡 *volumes: 몇 개의 숫자가 들어오든 하나의 묶음(Tuple)으로 쓸어 담습니다.
def master_mixer(*volumes):
    # sum() 함수를 이용해 바구니에 담긴 모든 트랙의 볼륨을 더합니다.
    total_volume = sum(volumes)
    
    # 최종 계산된 마스터 볼륨값을 반환합니다.
    return total_volume

# 2. 시스템 가동 및 결과값 수집 (System Execution)
# [Scene 1] 오케스트라 파트: 3개의 트랙 입력
scene_1 = master_mixer(70, 60, 50)

# [Scene 2] 일렉트로닉 파트: 5개의 트랙 입력
scene_2 = master_mixer(80, 90, 85, 40, 30)

# 3. 최종 결과 출력 (Final Report)
print(f"[Scene 1] 오케스트라 씬의 마스터 볼륨은 {scene_1} 입니다.")
print(f"[Scene 2] 일렉트로닉 씬의 마스터 볼륨은 {scene_2} 입니다.")