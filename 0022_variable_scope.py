# 0022_variable_scope.py (변수 생존 범위)
# 목적: 함수 안과 밖의 변수 생존 범위(Scope: 전역 변수와 지역 변수) 차이 이해하기
# -------------------------------------------------------
# Scenario:
# - 미디어 아트 전시장의 '메인 관제 시스템(전역)'과 'A구역 전시실(지역)'이 있음.
# - 메인 관제 시스템은 오늘 하루 전체 누적 관람객 수를 기록함 (전광판).
# - A구역 전시실(함수) 안에서는 방에 입장한 현재 인원만 일시적으로 계산함 (화이트보드).
# - 전시실 안에서 만든 데이터(지역 변수)는 방을 나가는 순간 사라진다는 것을 테스트함.
# -------------------------------------------------------

# 1. 메인 관제 시스템의 데이터 (전역 변수 Global Variable)
# 전시장의 모든 구역(함수)에서 이 전광판의 숫자를 볼 수 있습니다.
total_visitors = 1000

# 2. 개별 전시실 시스템 정의 
def update_zone_a(new_people):
    # 💡 지역 변수(Local Variable): 이 방 안에서만 살아있는 화이트보드 데이터
    zone_a_current = new_people
    
    # 방 안에서는 방 안의 변수도, 바깥의 전역 변수도 모두 볼 수 있습니다.
    print(f"[A구역 전시실] 현재 인원: {zone_a_current}명, 누적 관람객 수: {total_visitors}명")

# 3. 시스템 가동
# A구역에 50명이 새로 들어옵니다. (함수 실행)
update_zone_a(50)

# 4. [💡 핵심 테스트] 방 밖에서 방 안의 화이트보드(지역 변수) 훔쳐보기
print("\n[메인 관제실] A구역의 현재 인원을 확인해보겠습니다.")

# 🚨 아래 코드는 에러(NameError)를 발생시킵니다!
# 이유: zone_a_current는 함수가 끝나는 순간 사라지는 '지역 변수'이기 때문입니다.
# 방 안의 데이터를 밖에서 쓰려면 반드시 'return'을 사용해 던져주어야 합니다.
